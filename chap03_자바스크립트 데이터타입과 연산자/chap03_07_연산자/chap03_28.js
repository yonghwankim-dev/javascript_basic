/**
 * title : + 연산자 예제
 * 
 * + 연산자 종류
 * - 더하기 연산자
 * - 문자열 연결 연산자
 * 
 * 두 연산자가 모두 숫자인 경우 더하기 연산 수행
 * 두 연산자중 하나가 문자열인 경우 문자열 연결 연산이 수행
 * 
 */

let add1 = 1+2;
let add2 = "my" + "string";
let add3 = 1 + "string";
let add4 = "string" + 2;

console.log(add1);  // 3
console.log(add2);  // mystring
console.log(add3);  // 1string
console.log(add4);  // string2


