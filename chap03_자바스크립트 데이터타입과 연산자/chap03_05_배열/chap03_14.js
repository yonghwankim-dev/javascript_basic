// 배열 리터럴을 통한 5개 원소를 가진 배열 생성
let colorArr = ["orange", 'yellow', 'blue', 'green', 'red'];
console.log(colorArr[0]);   // orange
console.log(colorArr[1]);   // yellow
console.log(colorArr[4]);   // red